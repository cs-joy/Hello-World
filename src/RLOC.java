public class RLOC {
    // RLOC - Relational & Logical Operators - Conditionas
    public static void main(String[] args) {
        int a = 4;

        a = a + 2; // a += 2;
        a++; // increment // a += 1; // a = a + 1;
        System.out.println(a); // 7

        a = a - 2; // a -= 2;
        a--; // decrement // a = a-1; // a -= 1;
        System.out.println(a); // 4

        a = a * 2; // a *= 2;
        System.out.println(a); // 8

        a = a / 2; // a /= 2;
        System.out.println(a); // 4

    }
}
